// renderMethodsFile renders the full source of methods.gen.go: one
// TelegramBot method per spec method item, printing a summary of how many
// generated and how many fell back to an unresolved json.RawMessage return
// (see parseReturnType) to stdout as it goes.
func renderMethodsFile(apiVersion string, items []Item) string {
	var typeAndUnionNames []string
	for _, it := range items {
		if it.Kind == "type" || it.Kind == "union" {
			typeAndUnionNames = append(typeAndUnionNames, it.Name)
		}
	}

	var b strings.Builder
	fmt.Fprintf(&b, "// Code generated by internal/gen from Bot API %s. DO NOT EDIT.\n", apiVersion)
	b.WriteString("// Regenerate with: go generate ./...\n\n")
	b.WriteString("package golagram\n\n")
	b.WriteString("import (\n\t\"bytes\"\n\t\"context\"\n\t\"encoding/json\"\n\t\"fmt\"\n)\n\n")

	var generatedCount int
	var unresolved []string

	for _, it := range items {
		if it.Kind != "method" {
			continue
		}
		generatedCount++
		if renderMethod(&b, it, typeAndUnionNames) == "raw" {
			unresolved = append(unresolved, it.Name)
		}
	}

	fmt.Printf("methods.gen.go: %d generated\n", generatedCount)
	if len(unresolved) > 0 {
		fmt.Printf("methods.gen.go: %d method(s) with an unresolved return type (json.RawMessage, needs a look):\n", len(unresolved))
		for _, name := range unresolved {
			fmt.Printf("  - %s\n", name)
		}
	}

	return b.String()
}

// methodGoName capitalizes a Bot API method name for use as a TelegramBot
// method: "sendMessage" -> "SendMessage".
func methodGoName(apiName string) string {
	return strings.ToUpper(apiName[:1]) + apiName[1:]
}

// renderMethod emits one TelegramBot method for it: its request struct (if
// it has params), doc comment, and body — decode logic chosen by kind, as
// resolved by parseReturnType. Returns kind, so the caller can track how
// many methods fell back to "raw".
func renderMethod(b *strings.Builder, it Item, typeAndUnionNames []string) string {
	goName := methodGoName(it.Name)
	returnType, kind := parseReturnType(it.Returns, typeAndUnionNames)

	var reqType string
	if len(it.Params) > 0 {
		reqType = goName + "Request"
		renderRequestStruct(b, reqType, it)
	}

	b.WriteString(goDoc("", it.Description))
	if it.Returns != "" {
		fmt.Fprintf(b, "//\n// %s\n", strings.TrimSpace(it.Returns))
	}
	if kind == "raw" {
		b.WriteString("//\n// NOTE: this method's return type could not be confidently resolved from\n" +
			"// the spec prose, so it's left as json.RawMessage rather than guessed wrong.\n")
	}

	rawVar := "raw"
	if kind == "bool" {
		rawVar = "_"
	}

	if reqType != "" {
		fmt.Fprintf(b, "func (b *TelegramBot) %s(ctx context.Context, req *%s) (%s, error) {\n", goName, reqType, returnType)
		fmt.Fprintf(b, "\t%s, err := b.api.Call(ctx, %q, req)\n", rawVar, it.Name)
	} else {
		fmt.Fprintf(b, "func (b *TelegramBot) %s(ctx context.Context) (%s, error) {\n", goName, returnType)
		fmt.Fprintf(b, "\t%s, err := b.api.Call(ctx, %q, nil)\n", rawVar, it.Name)
	}
	b.WriteString("\tif err != nil {\n")
	fmt.Fprintf(b, "\t\treturn %s, err\n\t}\n", zeroValue(kind))

	switch kind {
	case "bool":
		b.WriteString("\treturn true, nil\n")
	case "raw":
		b.WriteString("\treturn raw, nil\n")
	case "array", "int", "string":
		fmt.Fprintf(b, "\tvar result %s\n", returnType)
		b.WriteString("\tif err := json.Unmarshal(raw, &result); err != nil {\n")
		fmt.Fprintf(b, "\t\treturn %s, fmt.Errorf(\"%s: failed to decode result: %%w\", err)\n\t}\n", zeroValue(kind), it.Name)
		if returnType == "[]Message" {
			b.WriteString("\tfor i := range result {\n\t\tb.bindMessage(ctx, &result[i])\n\t}\n")
		}
		b.WriteString("\treturn result, nil\n")
	case "object":
		// Some methods return the object on success but Telegram returns the
		// literal `true` instead when acting on an inline message — same
		// dual-return shape as the hand-written EditText.
		b.WriteString("\tif bytes.Equal(bytes.TrimSpace(raw), []byte(\"true\")) {\n\t\treturn nil, nil\n\t}\n")
		fmt.Fprintf(b, "\tvar result %s\n", strings.TrimPrefix(returnType, "*"))
		b.WriteString("\tif err := json.Unmarshal(raw, &result); err != nil {\n")
		fmt.Fprintf(b, "\t\treturn nil, fmt.Errorf(\"%s: failed to decode result: %%w\", err)\n\t}\n", it.Name)
		if returnType == "*Message" {
			b.WriteString("\tb.bindMessage(ctx, &result)\n")
		}
		b.WriteString("\treturn &result, nil\n")
	case "union":
		u := unionsBySpecName[strings.TrimPrefix(returnType, "*")]
		fmt.Fprintf(b, "\tresult, err := %s(raw)\n", unmarshalFuncName(u))
		b.WriteString("\tif err != nil {\n")
		fmt.Fprintf(b, "\t\treturn nil, fmt.Errorf(\"%s: failed to decode result: %%w\", err)\n\t}\n", it.Name)
		b.WriteString("\treturn result, nil\n")
	case "unionArray":
		u := unionsBySpecName[strings.TrimPrefix(returnType, "[]")]
		b.WriteString("\tvar raws []json.RawMessage\n")
		b.WriteString("\tif err := json.Unmarshal(raw, &raws); err != nil {\n")
		fmt.Fprintf(b, "\t\treturn nil, fmt.Errorf(\"%s: failed to decode result: %%w\", err)\n\t}\n", it.Name)
		fmt.Fprintf(b, "\tresult := make(%s, len(raws))\n", returnType)
		b.WriteString("\tfor i, r := range raws {\n")
		fmt.Fprintf(b, "\t\tv, err := %s(r)\n", unmarshalFuncName(u))
		b.WriteString("\t\tif err != nil {\n")
		fmt.Fprintf(b, "\t\t\treturn nil, fmt.Errorf(\"%s: failed to decode result[%%d]: %%w\", i, err)\n\t\t}\n", it.Name)
		b.WriteString("\t\tresult[i] = v\n\t}\n")
		b.WriteString("\treturn result, nil\n")
	}
	b.WriteString("}\n\n")

	return kind
}

// zeroValue returns the literal a generated method returns alongside a
// non-nil error, for the given parseReturnType kind.
func zeroValue(kind string) string {
	switch kind {
	case "bool":
		return "false"
	case "object", "array", "union", "unionArray":
		return "nil"
	case "int":
		return "0"
	case "string":
		return `""`
	default:
		return "nil"
	}
}

// renderRequestStruct emits reqType's struct definition (required params
// first, then optional) and its webhookMethod method — see
// webhookMethodNamer in webhook_reply.go.
func renderRequestStruct(b *strings.Builder, reqType string, it Item) {
	fmt.Fprintf(b, "// %s holds the parameters for %s.\n", reqType, it.Name)
	fmt.Fprintf(b, "type %s struct {\n", reqType)

	var required, optional []Field
	for _, p := range it.Params {
		if p.Required == "Yes" {
			required = append(required, p)
		} else {
			optional = append(optional, p)
		}
	}
	for _, p := range required {
		renderRequestField(b, reqType, p, false)
	}
	for _, p := range optional {
		renderRequestField(b, reqType, p, true)
	}
	b.WriteString("}\n\n")

	// webhookMethod satisfies the unexported webhookMethodNamer interface
	// (see webhook_reply.go) so Reply(req) can recover the Bot API method
	// name — "sendMessage", not "SendMessageRequest" — from the request
	// type alone, without a hand-maintained type->name table.
	fmt.Fprintf(b, "func (r *%s) webhookMethod() string { return %q }\n\n", reqType, it.Name)
}

// renderRequestField emits one request struct field: its doc comment,
// resolved type (see fieldTypeAndTag), and json tag.
func renderRequestField(b *strings.Builder, reqType string, p Field, optional bool) {
	goType, tag := fieldTypeAndTag(reqType, p.Type, p.Name, p.Description, optional)
	b.WriteString(goDoc("\t", p.Description))
	fmt.Fprintf(b, "\t%s %s `json:\"%s\"`\n", fieldName(p.Name), goType, tag)
}

// ---- return-type heuristics ----

var wordBoundaryCache = map[string]*regexp.Regexp{}

// matchesWord reports whether word occurs in text as a whole word (not as
// a substring of a longer identifier), caching the compiled regexp per word
// since parseReturnType calls this once per known type/union name for
// every method's returns text.
func matchesWord(text, word string) bool {
	re, ok := wordBoundaryCache[word]
	if !ok {
		re = regexp.MustCompile(`\b` + regexp.QuoteMeta(word) + `\b`)
		wordBoundaryCache[word] = re
	}
	return re.MatchString(text)
}

var arrayOfReturnRE = regexp.MustCompile(`(?i)array of ([A-Z][A-Za-z0-9]*)`)

// parseReturnType turns a method's free-text "returns" description into a Go
// return type, best-effort. kind drives the decode logic renderMethod emits:
// "array", "object" (pointer, with the True-or-object dual return handled),
// "union"/"unionArray" (decoded through the union's discriminator-switching
// unmarshal function), "bool", "int", "string", or "raw" (json.RawMessage,
// unresolved).
func parseReturnType(returns string, typeAndUnionNames []string) (goType, kind string) {
	if m := arrayOfReturnRE.FindStringSubmatch(returns); m != nil {
		if u, ok := unionsBySpecName[m[1]]; ok {
			if u.decodable {
				return "[]" + u.goName, "unionArray"
			}
			return "json.RawMessage", "raw" // can't decode this union yet
		}
		if resolved, ok := resolveBareName(m[1], typeAndUnionNames); ok {
			return "[]" + resolved, "array"
		}
	}

	best := ""
	for _, name := range typeAndUnionNames {
		if matchesWord(returns, name) && len(name) > len(best) {
			best = name
		}
	}
	if best != "" {
		if u, ok := unionsBySpecName[best]; ok {
			if u.decodable {
				return u.goName, "union"
			}
			return "json.RawMessage", "raw"
		}
		resolved, _ := resolveBareName(best, typeAndUnionNames)
		return "*" + resolved, "object"
	}

	if matchesWord(returns, "True") {
		return "bool", "bool"
	}
	if matchesWord(returns, "Integer") || matchesWord(returns, "Int") {
		return "int64", "int"
	}
	if matchesWord(returns, "String") {
		return "string", "string"
	}
	return "json.RawMessage", "raw"
}

// resolveBareName resolves a type/union name found in returns prose to its
// Go identifier — through aliasedTypes if it's an aliased type, or via
// goTypeName otherwise — reporting false if name isn't among known.
func resolveBareName(name string, known []string) (string, bool) {
	for _, n := range known {
		if n == name {
			if alias, ok := aliasedTypes[n]; ok {
				return alias, true
			}
			return goTypeName(n), true
		}
	}
	return "", false
}
