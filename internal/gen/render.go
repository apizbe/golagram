// renderTypesFile renders the full source of types.gen.go: the hand-coded
// ChatID preamble, every alias in aliasedTypes, then every spec type and
// union not in skipTypes.
func renderTypesFile(apiVersion string, items []Item) string {
	var b strings.Builder
	fmt.Fprintf(&b, "// Code generated by internal/gen from Bot API %s. DO NOT EDIT.\n", apiVersion)
	b.WriteString("// Regenerate with: go generate ./...\n\n")
	b.WriteString("package golagram\n\n")
	b.WriteString("import (\n\t\"context\"\n\t\"encoding/json\"\n\t\"fmt\"\n\t\"strings\"\n\n\t\"github.com/apizbe/golagram/internal/api\"\n)\n\n")

	b.WriteString("// ChatID identifies a chat either by its numeric ID or by the @username of a\n")
	b.WriteString("// public channel/supergroup. Construct with ChatIDFromInt or ChatIDFromUsername;\n")
	b.WriteString("// the zero value marshals as 0, which Telegram will reject, so always use one\n")
	b.WriteString("// of the constructors.\n")
	b.WriteString("type ChatID struct {\n\tid       int64\n\tusername string\n}\n\n")
	b.WriteString("// ChatIDFromInt targets a chat by its numeric ID.\n")
	b.WriteString("func ChatIDFromInt(id int64) ChatID { return ChatID{id: id} }\n\n")
	b.WriteString("// ChatIDFromUsername targets a public channel or supergroup by @username\n")
	b.WriteString("// (with or without the leading @).\n")
	b.WriteString("func ChatIDFromUsername(username string) ChatID {\n")
	b.WriteString("\treturn ChatID{username: strings.TrimPrefix(username, \"@\")}\n}\n\n")
	b.WriteString("func (c ChatID) MarshalJSON() ([]byte, error) {\n")
	b.WriteString("\tif c.username != \"\" {\n\t\treturn json.Marshal(\"@\" + c.username)\n\t}\n")
	b.WriteString("\treturn json.Marshal(c.id)\n}\n\n")
	b.WriteString("// IsZero reports whether c is the zero value (neither constructor called) —\n")
	b.WriteString("// used by encoding/json's `omitzero` tag on optional ChatID fields, since the\n")
	b.WriteString("// zero value's MarshalJSON output (0) is a real, rejectable chat_id rather\n")
	b.WriteString("// than something Telegram treats as absent, unlike `omitempty` (never true\n")
	b.WriteString("// for a non-pointer struct, whatever its contents).\n")
	b.WriteString("func (c ChatID) IsZero() bool { return c.id == 0 && c.username == \"\" }\n\n")

	for _, kv := range aliasEntries() {
		alias, orig := kv[0], kv[1]
		fmt.Fprintf(&b, "// %s is the Bot API's name for %s (see internal/gen/mapping.go:\n", alias, orig)
		fmt.Fprintf(&b, "// aliasedTypes for why it is flattened rather than modeled separately).\n")
		fmt.Fprintf(&b, "type %s = %s\n\n", alias, orig)
	}

	typeNames := map[string]bool{}
	unionNames := map[string]bool{}
	typesByName := map[string]Item{}
	for _, it := range items {
		switch it.Kind {
		case "type":
			typeNames[it.Name] = true
			typesByName[it.Name] = it
		case "union":
			unionNames[it.Name] = true
		}
	}

	for _, it := range items {
		if it.Kind != "type" || skipTypes[it.Name] {
			continue
		}
		renderStruct(&b, it)
	}

	// Some concrete types are members of more than one union (e.g. every
	// InputMedia* is also a member of InputPollMedia) — emittedAccessor stops
	// a shared field from getting a duplicate Get<Field>() method declared
	// on the same struct by a second union that happens to have it in common
	// too.
	emittedAccessor := map[string]bool{}
	for _, it := range items {
		if it.Kind != "union" {
			continue
		}
		if _, aliased := aliasedTypes[it.Name]; aliased {
			continue // flattened (MaybeInaccessibleMessage → Message)
		}
		renderUnion(&b, it, typeNames, unionNames, typesByName, emittedAccessor)
	}

	renderUnionUnmarshalers(&b)

	return b.String()
}

// aliasEntries returns aliasedTypes as (alias, original) pairs, sorted by
// alias name for deterministic output.
func aliasEntries() [][2]string {
	var out [][2]string
	for k, v := range aliasedTypes {
		out = append(out, [2]string{k, v})
	}
	sort.Slice(out, func(i, j int) bool { return out[i][0] < out[j][0] })
	return out
}

// renderStruct emits one spec type as a Go struct: its doc comment, any
// extraStructFields, every field with its resolved type and json tag, and
// — if any field is union-typed — its polymorphic UnmarshalJSON via
// renderStructUnmarshaler.
func renderStruct(b *strings.Builder, it Item) {
	b.WriteString(goDoc("", it.Description))
	fmt.Fprintf(b, "type %s struct {\n", goTypeName(it.Name))

	if extras := extraStructFields[it.Name]; len(extras) > 0 {
		b.WriteString("\t// Unexported bindings set by the dispatcher (see bot.go: bindMessage);\n")
		b.WriteString("\t// the sugar methods in the hand-written files use them.\n")
		for _, line := range extras {
			fmt.Fprintf(b, "\t%s\n", line)
		}
		b.WriteString("\n")
	}

	var unionFields []unionFieldRef
	for _, f := range it.Fields {
		goType, tag := fieldTypeAndTag(it.Name, f.Type, f.Name, f.Description, isOptionalTypeField(f))
		b.WriteString(goDoc("\t", f.Description))
		fmt.Fprintf(b, "\t%s %s `json:\"%s\"`\n", fieldName(f.Name), goType, tag)

		if u, isSlice := unionFieldFor(f.Type); u != nil {
			unionFields = append(unionFields, unionFieldRef{
				goName:    fieldName(f.Name),
				jsonName:  f.Name,
				union:     u,
				isSlice:   isSlice,
				optional:  isOptionalTypeField(f),
				decodable: u.decodable,
			})
		}
	}
	b.WriteString("}\n\n")

	renderStructUnmarshaler(b, it, unionFields)
}

// renderStructUnmarshaler emits an UnmarshalJSON for a struct with
// union-typed fields: encoding/json cannot decode into a bare interface, so
// those fields are captured as json.RawMessage and routed through the
// per-union unmarshal functions. Only emitted when at least one union field
// is decodable — a struct whose only union fields belong to input-only,
// non-decodable unions (InlineQueryResult*'s InputMessageContent) is never
// decoded from a Telegram response, so it keeps stdlib behavior.
func renderStructUnmarshaler(b *strings.Builder, it Item, unionFields []unionFieldRef) {
	anyDecodable := false
	for _, f := range unionFields {
		if f.decodable {
			anyDecodable = true
		}
	}
	if !anyDecodable {
		return
	}

	name := goTypeName(it.Name)
	fmt.Fprintf(b, "// UnmarshalJSON decodes %s's union-typed fields polymorphically —\n", name)
	b.WriteString("// encoding/json cannot unmarshal into a bare interface, so each is decoded\n")
	b.WriteString("// through its union's discriminator-switching unmarshal function.\n")
	fmt.Fprintf(b, "func (v *%s) UnmarshalJSON(data []byte) error {\n", name)
	fmt.Fprintf(b, "\ttype alias %s // methodless copy: avoids recursing into this UnmarshalJSON\n", name)
	b.WriteString("\taux := struct {\n\t\t*alias\n")
	for _, f := range unionFields {
		// Shadow every union field (even non-decodable ones) so stdlib
		// decoding never touches the interface field underneath.
		fmt.Fprintf(b, "\t\t%s json.RawMessage `json:\"%s\"`\n", f.goName, f.jsonName)
	}
	b.WriteString("\t}{alias: (*alias)(v)}\n")
	b.WriteString("\tif err := json.Unmarshal(data, &aux); err != nil {\n\t\treturn err\n\t}\n")

	for _, f := range unionFields {
		if !f.decodable {
			fmt.Fprintf(b, "\t// %s left nil: %s has no unambiguous discriminator (input-only union).\n", f.goName, f.union.goName)
			continue
		}
		fmt.Fprintf(b, "\tif len(aux.%s) > 0 && string(aux.%s) != \"null\" {\n", f.goName, f.goName)
		if f.isSlice {
			b.WriteString("\t\tvar raws []json.RawMessage\n")
			fmt.Fprintf(b, "\t\tif err := json.Unmarshal(aux.%s, &raws); err != nil {\n", f.goName)
			fmt.Fprintf(b, "\t\t\treturn fmt.Errorf(\"%s.%s: %%w\", err)\n\t\t}\n", name, f.jsonName)
			fmt.Fprintf(b, "\t\tout := make([]%s, len(raws))\n", f.union.goName)
			b.WriteString("\t\tfor i, r := range raws {\n")
			fmt.Fprintf(b, "\t\t\tu, err := %s(r)\n", unmarshalFuncName(f.union))
			fmt.Fprintf(b, "\t\t\tif err != nil {\n\t\t\t\treturn fmt.Errorf(\"%s.%s[%%d]: %%w\", i, err)\n\t\t\t}\n", name, f.jsonName)
			b.WriteString("\t\t\tout[i] = u\n\t\t}\n")
			fmt.Fprintf(b, "\t\tv.%s = out\n", f.goName)
		} else {
			fmt.Fprintf(b, "\t\tu, err := %s(aux.%s)\n", unmarshalFuncName(f.union), f.goName)
			fmt.Fprintf(b, "\t\tif err != nil {\n\t\t\treturn fmt.Errorf(\"%s.%s: %%w\", err)\n\t\t}\n", name, f.jsonName)
			fmt.Fprintf(b, "\t\tv.%s = u\n", f.goName)
		}
		b.WriteString("\t}\n")
	}
	b.WriteString("\treturn nil\n}\n\n")
}

// renderUnion emits one spec union as a marker interface (is<Name>()) plus
// a Get<Field>() accessor for every field its members have in common (see
// unionCommonFields), then the is<Name>()/Get<Field>() method bodies for
// each member type — deduplicated via emittedAccessor for members shared
// by more than one union.
func renderUnion(b *strings.Builder, it Item, typeNames, unionNames map[string]bool, typesByName map[string]Item, emittedAccessor map[string]bool) {
	name := goTypeName(it.Name)
	common := unionCommonFields(it, typeNames, typesByName)

	b.WriteString(goDoc("", it.Description))
	fmt.Fprintf(b, "type %s interface {\n\tis%s()\n", name, name)
	for _, f := range common {
		fmt.Fprintf(b, "\tGet%s() %s\n", f.goName, f.goType)
	}
	b.WriteString("}\n\n")

	for _, member := range it.Members {
		memberGo := goTypeName(member)
		if !typeNames[member] && !skipTypes[member] && !unionNames[member] {
			fmt.Fprintf(b, "// NOTE: union member %q for %s was not found among generated or\n"+
				"// hand-written types when this file was generated — check scripts/api.json.\n",
				member, name)
			continue
		}
		fmt.Fprintf(b, "func (*%s) is%s() {}\n", memberGo, name)
	}
	b.WriteString("\n")

	if len(common) > 0 {
		fmt.Fprintf(b, "// The Get* methods below implement %s: fields shared by every member,\n", name)
		b.WriteString("// callable on the interface value directly without a type switch.\n")
		for _, member := range it.Members {
			if !typeNames[member] {
				continue
			}
			memberGo := goTypeName(member)
			for _, f := range common {
				key := memberGo + ".Get" + f.goName
				if emittedAccessor[key] {
					continue // already satisfies this union's interface via an earlier union's identical accessor
				}
				emittedAccessor[key] = true
				fmt.Fprintf(b, "func (v *%s) Get%s() %s { return v.%s }\n", memberGo, f.goName, f.goType, f.goName)
			}
		}
		b.WriteString("\n")
	}
}

// ---- consts.gen.go ----

// renderConstsFile renders the full source of consts.gen.go: one `const
// ( ... )` block per enumGroup, each preceded by its Doc string.
func renderConstsFile(apiVersion string, groups []enumGroup) string {
	var b strings.Builder
	fmt.Fprintf(&b, "// Code generated by internal/gen from Bot API %s. DO NOT EDIT.\n", apiVersion)
	b.WriteString("// Regenerate with: go generate ./...\n\n")
	b.WriteString("package golagram\n\n")

	for _, g := range groups {
		b.WriteString(goDoc("", g.Doc))
		b.WriteString("const (\n")
		for _, m := range g.Members {
			fmt.Fprintf(&b, "\t%s%s = %q\n", g.GoPrefix, m.Name, m.Value)
		}
		b.WriteString(")\n\n")
	}

	return b.String()
}

